# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def merge(list1, list2):
    result = list()
    while len(list1) != 0 and len(list2) != 0:
        if list1[0] < list2[0]:
            result.append(list1.pop(0))
        else:
            result.append(list2.pop(0))
    result.extend(list1)
    result.extend(list2)
    return result


print(merge([1, 2, 3], [5, 6, 7, 8]))
